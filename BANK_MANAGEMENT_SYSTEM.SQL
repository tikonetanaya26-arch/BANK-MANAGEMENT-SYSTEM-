----- DISPLAY ALL CUSTOMERS ---
SELECT * FROM BANK_CUST;

----- DISPLAY CUSTOMERS FROM PUNE -----
SELECT * FROM BANK_CUST
WHERE CITY = 'PUNE';

----- DISPLAY ACC WITH BALANCE GREATER THAN 60000 ------
SELECT * FROM ACCOUNT
WHERE BALANCE > 60000;

---- DISPLAY TOTAL BANK BALANCE ----
SELECT SUM(BALANCE) AS TOTAL_BALANCE
FROM ACCOUNT;

---- DISPLAY AVG BALANCE ----
SELECT AVG(BALANCE) AS AVG_BALANCE
FROM ACCOUNT;

---- DISPLAY HIGHEST BALANCE ----
SELECT MAX(BALANCE) AS MAX_BALANCE 
FROM ACCOUNT;

---- DISPLAY LOWEST BALANCE --- 
SELECT MIN(BALANCE) AS MIN_BALANCE
FROM ACCOUNT;

---- DISPLAY COUNT TOTAL CUSTOMERS ---
SELECT COUNT(*) AS TOTAL_CUSTOMERS
FROM BANK_CUST;

---- CUSTOMER NAME WITH ACC BALANCE ---
SELECT c.CUSTOMER_NAME,
       a.ACCOUNT_TYPE,
       a.BALANCE 
FROM BANK_CUST c
INNER JOIN ACCOUNT a 
ON c.CUSTOMER_ID = a.CUSTOMER_ID;

----- BRANCH-WISE TOTAL BALANCE ------
SELECT BRANCH_ID ,
SUM(BALANCE) AS TOTAL_BALANCE 
FROM ACCOUNT
GROUP BY BRANCH_ID;

----- HAVING CLAUSE -----
SELECT BRANCH_ID ,
SUM(BALANCE) AS TOTAL_BALANCE 
FROM ACCOUNT
GROUP BY BRANCH_ID
HAVING SUM(BALANCE) > 100000;

---- ACCOUNTS HAVING BALANCE MORE THAN AVG ----
SELECT * FROM ACCOUNT 
 WHERE BALANCE > 
(
  SELECT AVG(BALANCE) 
  FROM ACCOUNT
);

---- LIKE OPERATOR ----
SELECT * FROM BANK_CUST
WHERE CUSTOMER_NAME LIKE 'A%';

---- IN OPERATOR ----
SELECT * FROM BANK_CUST
WHERE CITY IN ('PUNE','MUMBAI');

---- BETWEEN OPERATOR -----
SELECT * FROM ACCOUNT
WHERE BALANCE BETWEEN 40000 AND 80000;


---- RANK ACCOUNTS ACCORDING TO BALANCE ---
SELECT ACCOUNT_ID,
       BALANCE,
       RANK() OVER(ORDER BY BALANCE DESC) AS RANKING 
FROM ACCOUNT;



